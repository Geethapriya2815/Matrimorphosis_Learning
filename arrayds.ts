let purchaselist =["banana","apple","mango","orange"]
console.log(purchaselist)
purchaselist.push("pineapple") //add an element to the last
console.log(purchaselist)
purchaselist.unshift("grapes") //add element to the first
console.log(purchaselist)
purchaselist.pop()//remove the last element
console.log(purchaselist)
purchaselist.shift() //remove the first element
console.log(purchaselist)
